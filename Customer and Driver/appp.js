function addRow() {
          
    var src = document.getElementById("srcc");
    var dest = document.getElementById("destt");
	var date=document.getElementById("dates");
	var fare=document.getElementById("fares");
    var table = document.getElementById("myTableData");
 
    var rowCount = table.rows.length;
    var row = table.insertRow(rowCount);
 
    row.insertCell(0).innerHTML=date.value;
    row.insertCell(1).innerHTML= src.value;
	row.insertCell(2).innerHTML= dest.value;
	row.insertCell(3).innerHTML= fare.value;
   
 
}
 

 
function addTable() {
      
    var myTableDiv = document.getElementById("myDynamicTable");
      
    var table = document.createElement('TABLE');
    table.border='1';
    
    var tableBody = document.createElement('TBODY');
    table.appendChild(tableBody);
      
    for (var i=0; i<3; i++){
       var tr = document.createElement('TR');
       tableBody.appendChild(tr);
       
       for (var j=0; j<4; j++){
           var td = document.createElement('TD');
           td.width='75';
           td.appendChild(document.createTextNode("Cell " + i + "," + j));
           tr.appendChild(td);
       }
    }
    myTableDiv.appendChild(table);
    
}
 
